const WINTER_BRANCHES: EarthlyBranch[] = ['해', '자', '축']
const SUMMER_BRANCHES: EarthlyBranch[] = ['사', '오', '미']

/** 일간·월지로 조후용신을 구한다. */
export function computeJohu(
  dayGan: HeavenlyStem,
  monthBranch: EarthlyBranch,
): JohuYongSin {
  const rawStems = JOHU_RAW[dayGan][monthBranch]
  const ohaeng = rawStems.map(stemToOhaeng)
  const primary = ohaeng[0]
  const secondary = [...new Set(ohaeng.slice(1))].filter((o) => o !== primary)
  const urgent =
    (WINTER_BRANCHES.includes(monthBranch) && primary === '화') ||
    (SUMMER_BRANCHES.includes(monthBranch) && primary === '수')

  return { primary, secondary, rawStems, urgent }
}

/**
 * 억부용신 오행과 조후용신의 관계.
 * - aligned: 조후 1순위 = 억부용신
 * - partial: 억부용신이 조후 보조에 있거나 조후와 상생 관계
 * - conflict: 조후 1순위와 억부용신이 상극 관계
 */
export function yongSinRelation(
  johu: JohuYongSin,
  eokbuYongSin: Ohaeng,
): YongSinRelation {
  if (johu.primary === eokbuYongSin) return 'aligned'
  if (johu.secondary.includes(eokbuYongSin)) return 'partial'
  if (
    SAENG[johu.primary] === eokbuYongSin ||
    SAENG[eokbuYongSin] === johu.primary
  ) {
    return 'partial'
  }
  // 남은 관계는 상극(GEUK 어느 방향이든)
  if (
    GEUK[johu.primary] === eokbuYongSin ||
    GEUK[eokbuYongSin] === johu.primary
  ) {
    return 'conflict'
  }
  return 'partial'
}
